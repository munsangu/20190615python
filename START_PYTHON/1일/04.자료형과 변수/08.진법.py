# octal(8진수) : 0ct(숫자), 0o75
# hexadecimal(16진수) : hex(숫자), 0xAB
# demical(10진수) : 그냥 적으면 10진수로 변환
print(" 2진수 :", bin(0b1111011))
print(" 8진수 :", oct(0b1111011))
print("16진수 :", hex(0b1111011))
print("10진수 :", 0b1111011) #10진수로 변환
